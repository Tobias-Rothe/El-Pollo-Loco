/**
 * Represents a keyboard with various key states.
 *
 * @class KeyBoard
 * @property {boolean} LEFT - Indicates if the left arrow key is pressed.
 * @property {boolean} RIGHT - Indicates if the right arrow key is pressed.
 * @property {boolean} UP - Indicates if the up arrow key is pressed.
 * @property {boolean} DOWN - Indicates if the down arrow key is pressed.
 * @property {boolean} SPACE - Indicates if the space bar is pressed.
 * @property {boolean} D - Indicates if the 'D' key is pressed.
 * @property {boolean} M - Indicates if the 'M' key is pressed.
 */
class KeyBoard {
  LEFT = false;
  RIGHT = false;
  UP = false;
  DOWN = false;
  SPACE = false;
  D = false;
  M = false;
}
